package com.ash.pp.codesample.model;

public class EmployeeMeetingInteraction {

    private int employeeMeetingInteractionId;

    private int employeeId;

    private int meetingId;

    public int getEmployeeMeetingInteractionId() {
        return employeeMeetingInteractionId;
    }

    public void setEmployeeMeetingInteractionId(int employeeMeetingInteractionId) {
        this.employeeMeetingInteractionId = employeeMeetingInteractionId;
    }

    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    public int getMeetingId() {
        return meetingId;
    }

    public void setMeetingId(int meetingId) {
        this.meetingId = meetingId;
    }
}
